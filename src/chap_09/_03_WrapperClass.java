package chap_09;

public class _03_WrapperClass {
    public static void main(String[] args) {
        // wrapper 클래스
        // 기본 자료형 : int double float char

        Integer i = 123;
        Double d = 1.0;
        Character c = 'A';

        System.out.println(i.intValue());
        System.out.println(d.intValue());
        System.out.println(c.charValue());

    }
}
